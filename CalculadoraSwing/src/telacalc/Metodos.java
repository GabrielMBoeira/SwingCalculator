public class Metodos {
    
    public Metodos(){

}
    public Double somarValoresCalc(Double numero1, Double numero2){
        Double resultado = numero1 + numero2;
        return resultado;
    }
    public Double subtrairValoresCalc(Double numero1, Double numero2){
        Double resultado = numero1 - numero2;
        return resultado;
    }
    public Double dividirValoresCalc(Double numero1, Double numero2){
        Double resultado = numero1 / numero2;
        return resultado;
    }
    public Double multiplicarValoresCalc(Double numero1, Double numero2){
        Double resultado = numero1 * numero2;
        return resultado;
    }
 
    
}


